extension UIColor {
    public class var primaryOrange : UIColor {
        return UIColor(red: 243/255, green: 147/255, blue: 91/255, alpha: 1)
    }
    
    public class var secondGrey : UIColor {
        return UIColor(red: 150/255, green: 150/255, blue: 150/255, alpha: 1)
    }
}
